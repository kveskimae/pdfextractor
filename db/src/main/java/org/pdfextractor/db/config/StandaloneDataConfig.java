package org.pdfextractor.db.config;

import org.springframework.context.annotation.Configuration;
import org.springframework.jdbc.datasource.embedded.EmbeddedDatabase;
import org.springframework.jdbc.datasource.embedded.EmbeddedDatabaseBuilder;
import org.springframework.jdbc.datasource.embedded.EmbeddedDatabaseType;

import javax.sql.DataSource;

@Configuration
@TestEnvironment
public class StandaloneDataConfig implements DataConfig {

	public DataSource dataSource() {
		EmbeddedDatabaseBuilder builder = new EmbeddedDatabaseBuilder();
		EmbeddedDatabase db = builder.setType(EmbeddedDatabaseType.HSQL).
				addScript("sql/initialize_in_memory_db.sql").
				addScript("sql/2_create_schema.sql").
				addScript("sql/create_sequences.sql").
				addScript("sql/5_add_data.sql").
				build();
		// db.shutdown()
		return db;
	}

}
