package org.pdfextractor.dto.domain;

import org.pdfextractor.db.domain.dictionary.AccountType;
import org.apache.commons.lang3.builder.ReflectionToStringBuilder;

public class SecurityUserDTO {

	private Integer id;

	private String username;

	private String password;

	private String email;

	private boolean enabled;

	private AccountType accountType;

	private Integer trialLimit;

	public SecurityUserDTO() {
	}

	@Override
	public String toString() {
		return ReflectionToStringBuilder.toString(this);
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public boolean isEnabled() {
		return enabled;
	}

	public void setEnabled(boolean enabled) {
		this.enabled = enabled;
	}

	public AccountType getAccountType() {
		return accountType;
	}

	public void setAccountType(AccountType accountType) {
		this.accountType = accountType;
	}

	public Integer getTrialLimit() {
		return trialLimit;
	}

	public void setTrialLimit(Integer trialLimit) {
		this.trialLimit = trialLimit;
	}

}
