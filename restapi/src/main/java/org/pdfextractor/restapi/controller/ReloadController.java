package org.pdfextractor.restapi.controller;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.ApplicationContext;
import org.springframework.context.ConfigurableApplicationContext;
import org.springframework.http.HttpStatus;
import org.springframework.http.MediaType;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RestController;

@RestController
public class ReloadController {

	private static Logger log = LoggerFactory.getLogger(ReloadController.class);

	public static final String URL_PART = "reload";

	@Autowired
	private ApplicationContext applicationContext;

	@RequestMapping(value = "/" + ReloadController.URL_PART, method = RequestMethod.POST, produces = MediaType.APPLICATION_JSON_VALUE)
	public ResponseEntity<Object> reload() {
		log.info("Reloading context was requested");
		((ConfigurableApplicationContext)applicationContext).refresh();
		return new ResponseEntity<>(HttpStatus.OK);
	}

}
